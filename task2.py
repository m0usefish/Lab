#Вивести в таблицю квадрати й куби таких чисел: 1,3,5,9,17.
print("Квадрати і куби чисел: 1, 3, 5, 9, 17:")
print("Квадрат"," ","Куб")
for i in [1,3,5,9,17]:
    if i > 3 and i < 16:
        print(i ** 2, " " * 6, i ** 3)
    elif i > 16:
        print(i ** 2, " " * 5, i ** 3)
    else:
        print(i ** 2, " " * 7, i ** 3)

